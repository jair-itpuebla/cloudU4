<h1 class="informacion">¿Qué es cloud drive?</h1>
<section id="informacion">
	<p>Cloud drive es un servicio gratuito en la nube  en la cual se almacenan archivos, imágenes, videos, bases de datos y varios documentos que comúnmente guardamos en nuestra computadora.<br><br>En donde se ofrece un espacio libre de 2GB de almacenamiento.
	<br>
	<br>
	Cloud drive también te ayuda a respaldar, organizar y consultar todos tus archivos en cualquier momento y donde sea.</p>

